$success = 0
$failed = 0

foreach ($pdf in $allPdfs) {
    $url = if ($pdf.StartsWith("http")) { $pdf } else { "$baseUrl$pdf" }
    $decoded = [System.Web.HttpUtility]::UrlDecode($url)
    $filename = Split-Path $decoded -Leaf
    $filename = $filename -replace '[<>:"/\\|?*]', '_'
    $outPath = Join-Path $outDir $filename
    
    if (Test-Path $outPath) {
        Write-Host "SKIP (exists): $filename"
        $success++
        continue
    }
    
    try {
        Write-Host "Downloading: $filename"
        Invoke-WebRequest -Uri $url -OutFile $outPath -TimeoutSec 30 -ErrorAction Stop
        $success++
    } catch {
        Write-Host "FAILED: $filename - $_"
        $failed++
    }
}

